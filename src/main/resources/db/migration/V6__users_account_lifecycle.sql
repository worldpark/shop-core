-- -------------------------------------------------------------------------
-- status — 계정 상태 (ACTIVE/WITHDRAWN). NOT NULL, DEFAULT 'ACTIVE'
-- -------------------------------------------------------------------------
ALTER TABLE users
    ADD COLUMN status varchar(20) NOT NULL DEFAULT 'ACTIVE'
        CHECK (status IN ('ACTIVE', 'WITHDRAWN'));

-- -------------------------------------------------------------------------
-- deleted_at — 탈퇴 시각 (nullable, 활성 계정은 NULL)
-- -------------------------------------------------------------------------
ALTER TABLE users
    ADD COLUMN deleted_at timestamptz;
