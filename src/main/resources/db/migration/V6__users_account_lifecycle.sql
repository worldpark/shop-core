-- 탈퇴 시 users 행을 물리 삭제하지 않고 status=WITHDRAWN + deleted_at=탈퇴시각으로 처리.
-- 물리 삭제 금지: orders.member_id 등 FK 연관과 감사 추적 보존.
--
-- [이메일 재사용 정책]
-- email unique 제약 유지 — 탈퇴 행이 email을 점유하므로 동일 email 재가입 불가.
-- 재가입 허용 필요 시 별도 Task(부분 유니크 + email 익명화 정책 설계 필요).
